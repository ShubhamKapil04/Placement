#include<iostream>
using namespace std;

int main()
{
    vector<int> arr = { 3, 1, 2, 2};

    for(int i = 1; i < arr.size(); i++)
    {
        int temp = arr[i];
        int j = i - 1;
        for(; j >= 0; j--)
        {
            if(temp < arr[j])
            {
                arr[j+1] = arr[j];

            }
            else
            {
                break;
            }
        }
        arr[j+1] = temp;
    }
}


